# Take 2 numbers as input from user
# Print out all 4 math operators on those 2 numbers

x = int(input("Enter x as an integer : "))
y = int(input("Enter y  as an integer : "))

print(x)
print(y)

z = 0 # nothing

z = x + y
print(z)
print("The sum of ", x, " and ", y, " is ", z)

z = x - y
print(z)
print("The difference of ", x, " and ", y, " is ", z)

z = x * y
print(z)
print("The product of ", x, " and ", y, " is ", z)

z = x / y
print(z)
print("The division of of ", x, " and ", y, " is ", z)